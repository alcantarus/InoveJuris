-- 25. Contract Receivables Summary View
CREATE OR REPLACE VIEW contract_receivables_summary AS
SELECT 
    c.id AS contract_id,
    cl.name AS client_name,
    c."processNumber",
    c."contractValue" AS contract_value,
    c."amountReceived" AS amount_received,
    c."amountReceivable" AS amount_to_receive,
    (SELECT MIN("dueDate") FROM installments WHERE contract_id = c.id AND status NOT IN ('Quitado', 'Cancelada')) AS next_due_date,
    c.status AS contract_status,
    c.environment,
    c.organization_id
FROM 
    contracts c
JOIN 
    clients cl ON c.client_id = cl.id;

