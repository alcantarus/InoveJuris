-- 13. Logical Isolation & Audit Columns (Fallback for existing tables)
DO $$
DECLARE
  t text;
  tables text[] := ARRAY['users', 'clients', 'indicators', 'law_areas', 'products', 'contracts', 'installments', 'processes', 'bank_accounts', 'financial_categories', 'financial_transactions', 'audit_logs', 'roles', 'permissions', 'role_permissions', 'user_roles'];
BEGIN
  -- Fix identity columns to allow upserts
  FOREACH t IN ARRAY tables
  LOOP
    BEGIN
      EXECUTE format('ALTER TABLE %I ALTER COLUMN id SET GENERATED BY DEFAULT', t);
    EXCEPTION WHEN OTHERS THEN
      -- Ignore if already BY DEFAULT or column doesn't exist
    END;
  END LOOP;

  -- Fix audit_logs record_id constraint
  ALTER TABLE audit_logs ALTER COLUMN record_id DROP NOT NULL;

  -- Fix junction tables for audit system
  IF EXISTS (SELECT 1 FROM information_schema.tables WHERE table_name = 'role_permissions') THEN
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name = 'role_permissions' AND column_name = 'id') THEN
      ALTER TABLE role_permissions ADD COLUMN id BIGINT GENERATED BY DEFAULT AS IDENTITY;
      ALTER TABLE role_permissions DROP CONSTRAINT IF EXISTS role_permissions_pkey;
      ALTER TABLE role_permissions ADD PRIMARY KEY (id);
      ALTER TABLE role_permissions ADD CONSTRAINT role_permissions_role_id_permission_id_key UNIQUE (role_id, permission_id);
    END IF;
  END IF;

  IF EXISTS (SELECT 1 FROM information_schema.tables WHERE table_name = 'user_roles') THEN
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name = 'user_roles' AND column_name = 'id') THEN
      ALTER TABLE user_roles ADD COLUMN id BIGINT GENERATED BY DEFAULT AS IDENTITY;
      ALTER TABLE user_roles DROP CONSTRAINT IF EXISTS user_roles_pkey;
      ALTER TABLE user_roles ADD PRIMARY KEY (id);
      ALTER TABLE user_roles ADD CONSTRAINT user_roles_user_id_role_id_key UNIQUE (user_id, role_id);
    END IF;
  END IF;

  tables := ARRAY['users', 'clients', 'indicators', 'law_areas', 'products', 'contracts', 'installments', 'processes', 'bank_accounts', 'financial_categories', 'financial_transactions', 'audit_logs', 'roles', 'permissions', 'role_permissions', 'user_roles'];
  FOREACH t IN ARRAY tables
  LOOP
    EXECUTE format('ALTER TABLE %I ADD COLUMN IF NOT EXISTS environment TEXT DEFAULT ''production''', t);
    EXECUTE format('UPDATE %I SET environment = ''production'' WHERE environment IS NULL', t);
    EXECUTE format('ALTER TABLE %I ADD COLUMN IF NOT EXISTS created_by BIGINT REFERENCES users(id) ON DELETE SET NULL', t);
    EXECUTE format('ALTER TABLE %I ADD COLUMN IF NOT EXISTS updated_by BIGINT REFERENCES users(id) ON DELETE SET NULL', t);
  END LOOP;
END $$;

-- 14. Audit System (Function & Triggers)
CREATE OR REPLACE FUNCTION log_audit_event()
RETURNS TRIGGER AS $$
DECLARE
  old_data JSONB;
  new_data JSONB;
  changed_fields JSONB;
  performed_by BIGINT;
  record_id TEXT;
  v_env TEXT;
BEGIN
  IF (TG_OP = 'INSERT') THEN
    new_data = to_jsonb(NEW);
    old_data = null;
    changed_fields = null;
    
    IF new_data ? 'created_by' THEN
      performed_by = (new_data->>'created_by')::BIGINT;
    ELSE
      performed_by = NULL;
    END IF;
    
    IF new_data ? 'environment' AND new_data->>'environment' IS NOT NULL THEN
      v_env = new_data->>'environment';
    ELSE
      v_env = 'production';
    END IF;
    
    record_id = new_data->>'id';
  ELSIF (TG_OP = 'UPDATE') THEN
    new_data = to_jsonb(NEW);
    old_data = to_jsonb(OLD);
    SELECT jsonb_object_agg(key, value) INTO changed_fields
    FROM jsonb_each(new_data)
    WHERE old_data->key IS DISTINCT FROM value;
    
    IF new_data ? 'updated_by' THEN
      performed_by = (new_data->>'updated_by')::BIGINT;
    ELSIF new_data ? 'created_by' THEN
      performed_by = (new_data->>'created_by')::BIGINT;
    ELSE
      performed_by = NULL;
    END IF;
    
    IF new_data ? 'environment' AND new_data->>'environment' IS NOT NULL THEN
      v_env = new_data->>'environment';
    ELSE
      v_env = 'production';
    END IF;
    
    record_id = new_data->>'id';
  ELSIF (TG_OP = 'DELETE') THEN
    new_data = null;
    old_data = to_jsonb(OLD);
    changed_fields = null;
    performed_by = NULL;
    
    IF old_data ? 'environment' AND old_data->>'environment' IS NOT NULL THEN
      v_env = old_data->>'environment';
    ELSE
      v_env = 'production';
    END IF;
    
    record_id = old_data->>'id';
  END IF;

  INSERT INTO audit_logs (table_name, record_id, action, old_data, new_data, changed_fields, performed_by, environment)
  VALUES (TG_TABLE_NAME, record_id, TG_OP, old_data, new_data, changed_fields, performed_by, v_env);

  RETURN NULL;
END;
$$ LANGUAGE plpgsql;

-- Apply Audit Triggers
DO $$
DECLARE
  t text;
BEGIN
  FOR t IN SELECT table_name FROM information_schema.tables WHERE table_schema = 'public' AND table_type = 'BASE TABLE' AND table_name != 'audit_logs' AND table_name != 'system_settings'
  LOOP
    EXECUTE format('DROP TRIGGER IF EXISTS audit_%I ON %I', t, t);
    EXECUTE format('CREATE TRIGGER audit_%I AFTER INSERT OR UPDATE OR DELETE ON %I FOR EACH ROW EXECUTE FUNCTION log_audit_event()', t, t);
  END LOOP;
END $$;

-- 15. Cash Flow Balance System (Function & Trigger)
CREATE OR REPLACE FUNCTION update_account_balance()
RETURNS TRIGGER AS $$
BEGIN
  IF (TG_OP = 'INSERT') THEN
    IF (NEW.type = 'income') THEN
      UPDATE bank_accounts SET current_balance = current_balance + NEW.amount WHERE id = NEW.account_id;
    ELSIF (NEW.type = 'expense') THEN
      UPDATE bank_accounts SET current_balance = current_balance - NEW.amount WHERE id = NEW.account_id;
    ELSIF (NEW.type = 'transfer') THEN
      UPDATE bank_accounts SET current_balance = current_balance - NEW.amount WHERE id = NEW.account_id;
      UPDATE bank_accounts SET current_balance = current_balance + NEW.amount WHERE id = NEW.destination_account_id;
    END IF;
  ELSIF (TG_OP = 'DELETE') THEN
    IF (OLD.type = 'income') THEN
      UPDATE bank_accounts SET current_balance = current_balance - OLD.amount WHERE id = OLD.account_id;
    ELSIF (OLD.type = 'expense') THEN
      UPDATE bank_accounts SET current_balance = current_balance + OLD.amount WHERE id = OLD.account_id;
    ELSIF (OLD.type = 'transfer') THEN
      UPDATE bank_accounts SET current_balance = current_balance + OLD.amount WHERE id = OLD.account_id;
      UPDATE bank_accounts SET current_balance = current_balance - OLD.amount WHERE id = OLD.destination_account_id;
    END IF;
  ELSIF (TG_OP = 'UPDATE') THEN
    IF (OLD.type = 'income') THEN
      UPDATE bank_accounts SET current_balance = current_balance - OLD.amount WHERE id = OLD.account_id;
    ELSIF (OLD.type = 'expense') THEN
      UPDATE bank_accounts SET current_balance = current_balance + OLD.amount WHERE id = OLD.account_id;
    ELSIF (OLD.type = 'transfer') THEN
      UPDATE bank_accounts SET current_balance = current_balance + OLD.amount WHERE id = OLD.account_id;
      UPDATE bank_accounts SET current_balance = current_balance - OLD.amount WHERE id = OLD.destination_account_id;
    END IF;
    IF (NEW.type = 'income') THEN
      UPDATE bank_accounts SET current_balance = current_balance + NEW.amount WHERE id = NEW.account_id;
    ELSIF (NEW.type = 'expense') THEN
      UPDATE bank_accounts SET current_balance = current_balance - NEW.amount WHERE id = NEW.account_id;
    ELSIF (NEW.type = 'transfer') THEN
      UPDATE bank_accounts SET current_balance = current_balance - NEW.amount WHERE id = NEW.account_id;
      UPDATE bank_accounts SET current_balance = current_balance + NEW.amount WHERE id = NEW.destination_account_id;
    END IF;
  END IF;
  RETURN NULL;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trigger_update_balance ON financial_transactions;
CREATE TRIGGER trigger_update_balance AFTER INSERT OR UPDATE OR DELETE ON financial_transactions FOR EACH ROW EXECUTE FUNCTION update_account_balance();

-- 16. Row Level Security (RLS) & Policies
DO $$
BEGIN
  -- 1. Drop policies that depend on user_id
  EXECUTE 'DROP POLICY IF EXISTS "Usuários podem gerenciar suas próprias tarefas" ON tasks';
  EXECUTE 'DROP POLICY IF EXISTS "Usuários podem ver histórico de suas tarefas" ON task_history';

  -- 2. Alter column type and add environment column
  IF EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name = 'tasks' AND column_name = 'user_id' AND data_type = 'uuid') THEN
    ALTER TABLE tasks DROP CONSTRAINT IF EXISTS tasks_user_id_fkey;
    ALTER TABLE tasks ALTER COLUMN user_id TYPE TEXT;
  END IF;
  
  ALTER TABLE tasks ADD COLUMN IF NOT EXISTS environment TEXT DEFAULT 'production';
  UPDATE tasks SET environment = 'production' WHERE environment IS NULL;

  -- 3. Recreate policies
  EXECUTE 'DROP POLICY IF EXISTS "Allow all access to tasks" ON tasks';
  EXECUTE 'CREATE POLICY "Allow all access to tasks" ON tasks FOR ALL USING (true) WITH CHECK (true)';
  EXECUTE 'DROP POLICY IF EXISTS "Allow all access to task_history" ON task_history';
  EXECUTE 'CREATE POLICY "Allow all access to task_history" ON task_history FOR ALL USING (true) WITH CHECK (true)';
END $$;

-- Triggers para Imutabilidade de Contratos e Parcelas Quitadas
CREATE OR REPLACE FUNCTION prevent_update_delete_paid_contract()
RETURNS TRIGGER AS $$
BEGIN
  IF (OLD.status = 'Quitado') THEN
    RAISE EXCEPTION 'Não é possível alterar ou excluir um contrato quitado.';
  END IF;
  RETURN OLD;
END;
$$ LANGUAGE plpgsql;

