-- Function to process an installment payment
CREATE OR REPLACE FUNCTION process_installment_payment(
  p_installment_id BIGINT,
  p_amount_paid DECIMAL(12,2),
  p_account_id BIGINT,
  p_category_id BIGINT,
  p_date DATE,
  p_description TEXT,
  p_user_id BIGINT,
  p_interest DECIMAL(12,2),
  p_fine DECIMAL(12,2)
) RETURNS JSONB AS $$
DECLARE
  v_installment RECORD;
  v_contract_id BIGINT;
  v_total_due DECIMAL(12,2);
  v_diff DECIMAL(12,2);
  v_new_received DECIMAL(12,2);
  v_new_receivable DECIMAL(12,2);
  v_contract_value DECIMAL(12,2);
  v_new_status TEXT;
  v_all_paid BOOLEAN;
  v_some_paid BOOLEAN;
  v_has_estornado BOOLEAN;
BEGIN
  -- 1. Get installment and contract info
  SELECT * INTO v_installment FROM installments WHERE id = p_installment_id;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'Parcela não encontrada';
  END IF;
  
  v_contract_id := v_installment.contract_id;
  SELECT "contractValue" INTO v_contract_value FROM contracts WHERE id = v_contract_id;

  -- 2. Update current installment
  IF v_installment.status = 'Cancelado' THEN
    RAISE EXCEPTION 'Não é possível pagar uma parcela cancelada';
  END IF;

  v_total_due := (v_installment.amount - COALESCE(v_installment."amountPaid", 0)) + p_interest + p_fine;
  
  IF p_amount_paid >= v_total_due - 0.01 THEN
    -- Full payment
    UPDATE installments 
    SET status = 'Quitado', 
        "amountPaid" = COALESCE("amountPaid", 0) + p_amount_paid, 
        interest = COALESCE(interest, 0) + p_interest, 
        fine = COALESCE(fine, 0) + p_fine,
        updated_by = p_user_id
    WHERE id = p_installment_id;
  ELSE
    -- Partial payment
    UPDATE installments 
    SET status = 'Parcial', 
        "amountPaid" = COALESCE("amountPaid", 0) + p_amount_paid, 
        interest = COALESCE(interest, 0) + p_interest, 
        fine = COALESCE(fine, 0) + p_fine,
        updated_by = p_user_id
    WHERE id = p_installment_id;
  END IF;

  -- 3. Create financial transaction
  -- Note: trigger_update_balance will automatically update the bank account balance
  INSERT INTO financial_transactions (
    date, amount, type, account_id, category_id, description, environment, created_by
  ) VALUES (
    p_date, p_amount_paid, 'income', p_account_id, p_category_id, p_description, v_installment.environment, p_user_id
  );

  -- 4. Create payment record
  INSERT INTO payments (
    installment_id, amount, type, payment_date, account_id, category_id, description, environment, created_by
  ) VALUES (
    p_installment_id, p_amount_paid, 'payment', p_date, p_account_id, p_category_id, p_description, v_installment.environment, p_user_id
  );

  -- 5. Update contract totals and status
  SELECT COALESCE(SUM("amountPaid"), 0) INTO v_new_received FROM installments WHERE contract_id = v_contract_id;
  v_new_receivable := GREATEST(0, v_contract_value - v_new_received);
  
  SELECT 
    bool_and(status IN ('Quitado', 'Cancelado')) AND bool_or(status = 'Quitado'),
    bool_or("amountPaid" > 0),
    bool_or(status = 'Estornado')
  INTO v_all_paid, v_some_paid, v_has_estornado
  FROM installments 
  WHERE contract_id = v_contract_id;

  IF v_has_estornado THEN
    v_new_status := 'Estornado';
  ELSIF v_all_paid THEN
    v_new_status := 'Quitado';
  ELSIF v_some_paid THEN
    v_new_status := 'Parcial';
  ELSE
    v_new_status := 'Aberto';
  END IF;

  UPDATE contracts 
  SET "amountReceived" = v_new_received, 
      "amountReceivable" = v_new_receivable,
      status = v_new_status,
      updated_by = p_user_id
  WHERE id = v_contract_id;

  RETURN jsonb_build_object(
    'success', true,
    'new_received', v_new_received,
    'new_receivable', v_new_receivable,
    'new_status', v_new_status
  );
END;
$$ LANGUAGE plpgsql;

-- Function to process an installment estorno (reversal)
CREATE OR REPLACE FUNCTION process_installment_estorno(
  p_installment_id BIGINT,
  p_reason TEXT,
  p_account_id BIGINT,
  p_user_id BIGINT
) RETURNS JSONB AS $$
DECLARE
  v_installment RECORD;
  v_contract_id BIGINT;
  v_amount_to_reverse DECIMAL(12,2);
  v_new_received DECIMAL(12,2);
  v_new_receivable DECIMAL(12,2);
  v_contract_value DECIMAL(12,2);
  v_new_status TEXT;
  v_all_paid BOOLEAN;
  v_some_paid BOOLEAN;
  v_has_estornado BOOLEAN;
  v_old_obs TEXT;
  v_payment RECORD;
BEGIN
  -- 1. Get installment and contract info
  SELECT * INTO v_installment FROM installments WHERE id = p_installment_id;
  IF NOT FOUND THEN
    RAISE EXCEPTION 'Parcela não encontrada';
  END IF;
  
  IF v_installment."amountPaid" <= 0 THEN
    RAISE EXCEPTION 'Esta parcela não possui valor pago para estornar';
  END IF;

  v_contract_id := v_installment.contract_id;
  SELECT "contractValue", observations INTO v_contract_value, v_old_obs FROM contracts WHERE id = v_contract_id;
  v_amount_to_reverse := v_installment."amountPaid";

  -- 2. Update installment
  UPDATE installments 
  SET status = 'Estornado', 
      "amountPaid" = 0,
      interest = 0,
      fine = 0,
      updated_by = p_user_id
  WHERE id = p_installment_id;

  -- 3. Find the original payment record to delete the corresponding financial transaction
  SELECT * INTO v_payment FROM payments 
  WHERE installment_id = p_installment_id AND type = 'payment' 
  ORDER BY payment_date DESC LIMIT 1;

  IF FOUND THEN
    -- Delete the corresponding financial transaction (income)
    -- The trigger update_account_balance will automatically subtract the amount from the bank account
    DELETE FROM financial_transactions 
    WHERE id IN (
      SELECT id FROM financial_transactions 
      WHERE amount = v_payment.amount 
        AND date = v_payment.payment_date 
        AND account_id = v_payment.account_id 
        AND type = 'income'
        AND environment = v_installment.environment
      ORDER BY id DESC LIMIT 1
    );
  END IF;

  -- 4. Create reversal payment record
  INSERT INTO payments (
    installment_id, amount, type, payment_date, account_id, description, environment, created_by
  ) VALUES (
    p_installment_id, v_amount_to_reverse, 'reversal', CURRENT_DATE, p_account_id, 'Estorno: ' || p_reason, v_installment.environment, p_user_id
  );

  -- 5. Update contract totals and status
  SELECT COALESCE(SUM("amountPaid"), 0) INTO v_new_received FROM installments WHERE contract_id = v_contract_id;
  v_new_receivable := GREATEST(0, v_contract_value - v_new_received);
  
  SELECT 
    bool_and(status IN ('Quitado', 'Cancelado')) AND bool_or(status = 'Quitado'),
    bool_or("amountPaid" > 0),
    bool_or(status = 'Estornado')
  INTO v_all_paid, v_some_paid, v_has_estornado
  FROM installments 
  WHERE contract_id = v_contract_id;

  IF v_has_estornado THEN
    v_new_status := 'Estornado';
  ELSIF v_all_paid THEN
    v_new_status := 'Quitado';
  ELSIF v_some_paid THEN
    v_new_status := 'Parcial';
  ELSE
    v_new_status := 'Aberto';
  END IF;

  UPDATE contracts 
  SET "amountReceived" = v_new_received, 
      "amountReceivable" = v_new_receivable,
      status = v_new_status,
      observations = COALESCE(v_old_obs, '') || E'\n' || '[ESTORNO]: ' || p_reason,
      updated_by = p_user_id
  WHERE id = v_contract_id;

  RETURN jsonb_build_object(
    'success', true,
    'new_received', v_new_received,
    'new_receivable', v_new_receivable,
    'new_status', v_new_status
  );
END;
$$ LANGUAGE plpgsql;

-- Function to process contract cancellation
CREATE OR REPLACE FUNCTION process_contract_cancellation(
  p_contract_id BIGINT,
  p_reason TEXT,
  p_user_id BIGINT
) RETURNS JSONB AS $$
DECLARE
  v_has_payments BOOLEAN;
  v_old_obs TEXT;
BEGIN
  -- 1. Check if there are any invalid installments (paid, reversed, extended, etc.)
  SELECT COALESCE(bool_or("amountPaid" > 0 OR status NOT IN ('Aberto', 'Atrasada')), false) INTO v_has_payments 
  FROM installments 
  WHERE contract_id = p_contract_id;
  
  IF v_has_payments THEN
    RAISE EXCEPTION 'Não é possível cancelar um contrato com parcelas recebidas, estornadas ou prorrogadas.';
  END IF;

  -- 2. Update contract
  SELECT observations INTO v_old_obs FROM contracts WHERE id = p_contract_id;
  
  UPDATE contracts 
  SET status = 'Cancelado',
      observations = COALESCE(v_old_obs, '') || E'\n\n' || '[CANCELAMENTO]: ' || p_reason,
      updated_by = p_user_id
  WHERE id = p_contract_id;

  -- 3. Update all installments
  UPDATE installments 
  SET status = 'Cancelada',
      updated_by = p_user_id
  WHERE contract_id = p_contract_id;

  RETURN jsonb_build_object('success', true);
END;
$$ LANGUAGE plpgsql;

